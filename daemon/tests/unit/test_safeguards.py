"""Tests for the v2.6 safeguards module.

v2.6 phase 2: pure-function heuristics for the reindex pipeline.
``classify_file`` returns 'ok' / 'auto_skipped' / 'suspicious'; the
'malformed' classification is added by ingest in phase 3 when an
actual parse raises.
"""

from __future__ import annotations

import os
import secrets
from pathlib import Path

from mnemo import safeguards

# --- classify_file orchestration --------------------------------------------


def test_classify_ok_for_normal_markdown(tmp_path: Path) -> None:
    p = tmp_path / "doc.md"
    p.write_text("# Hello\n\nNormal docs with plain text.\n")
    c = safeguards.classify_file(p)
    assert c.category == "ok"
    assert c.reason is None


def test_classify_ok_for_normal_python(tmp_path: Path) -> None:
    p = tmp_path / "foo.py"
    p.write_text("def hello():\n    return 'world'\n")
    c = safeguards.classify_file(p)
    assert c.category == "ok"


def test_classify_unsupported_extension(tmp_path: Path) -> None:
    p = tmp_path / "image.xyz"
    p.write_text("not text")
    c = safeguards.classify_file(p)
    assert c.category == "auto_skipped"
    assert c.reason is not None
    assert c.reason.startswith("unsupported_ext")


def test_classify_oversize(tmp_path: Path) -> None:
    p = tmp_path / "big.md"
    p.write_bytes(b"x" * (6 * 1024 * 1024))  # 6 MiB > 5 MiB default
    c = safeguards.classify_file(p)
    assert c.category == "auto_skipped"
    assert c.reason is not None
    assert c.reason.startswith("oversize")


def test_classify_respects_custom_size_limit(tmp_path: Path) -> None:
    p = tmp_path / "small.md"
    p.write_bytes(b"x" * 200)
    c = safeguards.classify_file(p, size_limit_bytes=100)
    assert c.category == "auto_skipped"
    assert c.reason is not None
    assert "oversize" in c.reason


def test_classify_accepts_content_bytes_param(tmp_path: Path) -> None:
    """Pre-read content bypasses the read inside classify_file."""
    p = tmp_path / "doc.md"
    p.write_text("# Real content")
    c = safeguards.classify_file(p, content_bytes=b"# Different content")
    assert c.category == "ok"


# --- is_binary_masquerading_as_text -----------------------------------------


def test_binary_masquerade_detects_nul_heavy_content() -> None:
    # Build a sample with ~40% NUL bytes.
    content = b"".join(b"a\x00\x00" if i % 2 else b"b" for i in range(1000))
    assert safeguards.is_binary_masquerading_as_text(content) is not None


def test_binary_masquerade_passes_normal_text() -> None:
    assert safeguards.is_binary_masquerading_as_text(b"hello world\nthis is normal text\n") is None


def test_binary_masquerade_handles_empty_input() -> None:
    assert safeguards.is_binary_masquerading_as_text(b"") is None


def test_binary_masquerade_threshold_is_configurable() -> None:
    # 20% NUL ratio passes default 30% threshold, fails 10%.
    content = b"abcdefgh\x00\x00" * 100  # 20% nul
    assert safeguards.is_binary_masquerading_as_text(content, threshold=0.30) is None
    assert safeguards.is_binary_masquerading_as_text(content, threshold=0.10) is not None


