# --- is_high_entropy_blob ---------------------------------------------------


def test_high_entropy_detects_random_data() -> None:
    random_data = secrets.token_bytes(8192)
    reason = safeguards.is_high_entropy_blob(random_data, threshold=7.5)
    assert reason is not None


def test_high_entropy_passes_normal_text() -> None:
    text = b"Hello world. This is normal English text with low entropy.\n" * 200
    assert safeguards.is_high_entropy_blob(text, threshold=7.5) is None


def test_high_entropy_skips_tiny_samples() -> None:
    assert safeguards.is_high_entropy_blob(b"short", threshold=7.5) is None


def test_high_entropy_threshold_configurable() -> None:
    """Entropy of plain English is around 4-5; a low threshold must still flag it."""
    text = b"Hello world. This is normal English text with low entropy.\n" * 200
    # Even normal text has SOME entropy; a very-low threshold should catch it.
    reason = safeguards.is_high_entropy_blob(text, threshold=3.0)
    assert reason is not None


